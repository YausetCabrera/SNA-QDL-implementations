block_size = 100
samples    = 5000

def compute_frame(kappa):
    k_leak = int(round(lambda_leak * n))
    n_priv = max(n - k_leak, 1)

    F = np.random.randint(d, size=(n_priv, kappa))
    log_norm = -n_priv * math.log(d)

    def log_rho_element(a, b):
        diff = (a - b) % d
        for l in range(kappa):
            if int(np.dot(F[:, l], diff)) % d != 0:
                return -np.inf
        return log_norm

    def idx_to_vec(i, length):
        v = np.empty(length, dtype=np.int8)
        for j in range(length):
            v[j] = i % d; i //= d
        return v

    sz = min(block_size, d ** min(n_priv, 6))
    block_log = np.full((sz, sz), -np.inf)
    vecs = [idx_to_vec(i, n_priv) for i in range(sz)]
    for i in range(sz):
        for j in range(sz):
            block_log[i, j] = log_rho_element(vecs[i], vecs[j])

    log_vals = np.array([
        log_rho_element(
            np.random.randint(d, size=n_priv, dtype=np.int8),
            np.random.randint(d, size=n_priv, dtype=np.int8)
        ) for _ in range(samples)
    ])
    frac_nonzero = np.sum(np.isfinite(log_vals)) / samples
    return block_log, log_vals, n_priv, frac_nonzero, log_norm


