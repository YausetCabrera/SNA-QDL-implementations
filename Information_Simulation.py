def render_frame(frame_idx):
    kappa = kappa_list[frame_idx]
    block_log, log_vals, n_priv, frac_nz, log_norm = compute_frame(kappa)

    finite_vals = block_log[np.isfinite(block_log)]
    v_min = finite_vals.min() if len(finite_vals) > 0 else -1
    v_max = finite_vals.max() if len(finite_vals) > 0 else 0
    display = np.where(np.isfinite(block_log), block_log, v_min - 1)

    span  = max(v_max - (v_min - 1), 1e-12)
    norm_d = np.clip((display - (v_min - 1)) / span, 0, 1)

    rows, cols = norm_d.shape
    rr, cc = np.meshgrid(np.linspace(0, 1, rows),
                          np.linspace(0, 1, cols), indexing='ij')

    highlight  = 0.07 * np.exp(-3.0 * (rr + cc))
    sheen      = 0.05 * np.exp(-12.0 * (rr - cc) ** 2)
    shaded      = np.clip(norm_d + highlight + sheen, 0, 1)
    shaded_data = shaded * span + (v_min - 1)

    im.set_data(shaded_data)
    im.set_clim(vmin=v_min - 1, vmax=v_max + 1e-12)
    ax_img.set_title(
        r"$\log|\rho|$ map  —  $\kappa = $" + str(kappa),
        fontsize=11, fontweight="bold", color=ACCENT, pad=8)
    cbar.set_label(r"$\log|\rho(a,b)|$", fontsize=10, color=TEXT_DARK)

    # ── Histogram 
    ax_hist.cla()
    ax_hist.set_facecolor(PANEL_BG)
    for sp in ["top", "right"]:
        ax_hist.spines[sp].set_visible(False)
    for sp in ["left", "bottom"]:
        ax_hist.spines[sp].set_edgecolor(SPINE_COL)

    finite_log = log_vals[np.isfinite(log_vals)]
    pct_zero   = (1 - frac_nz) * 100

    if len(finite_log) > 0:
        ax_hist.hist(
            finite_log, bins=40,
            color="#60A5FA", edgecolor=PANEL_BG, linewidth=0.4, alpha=0.88,
            label=rf"$\rho_{{ab}} \neq 0$  $({frac_nz*100:.1f}\%)$")
        mean_v = finite_log.mean()
        ax_hist.axvline(
            mean_v, color=ACCENT, linewidth=1.8, linestyle="--",
            label=rf"$\mu = {mean_v:.2f}$")
        ax_hist.axvline(
            log_norm, color="#DC2626", linewidth=1.4, linestyle=":", alpha=0.8,
            label=rf"$\log(d^{{-n'}}) = {log_norm:.1f}$")
        ax_hist.legend(fontsize=8.5, frameon=True,
                       edgecolor=SPINE_COL, framealpha=0.95)
    else:
        ax_hist.text(
            0.5, 0.5,
            rf"All elements $= 0$" + "\n" + rf"($\kappa = {kappa}$, cancellation $= {pct_zero:.1f}\%$)",
            ha="center", va="center", transform=ax_hist.transAxes,
            fontsize=10, color=TEXT_MID)

    ax_hist.text(
        0.02, 0.97,
        rf"${pct_zero:.1f}\%$ null elements" + "\n" + r"(key cancellation)",
        transform=ax_hist.transAxes, fontsize=8, color=TEXT_MID,
        va="top", ha="left",
        bbox=dict(boxstyle="round,pad=0.3", facecolor=BG,
                  edgecolor=SPINE_COL, linewidth=0.7))

    ax_hist.set_title(
        r"Distribution of $\log|\rho_{ab}|$  —  $\kappa = $" + str(kappa),
        fontsize=11, fontweight="bold", color=ACCENT, pad=8)
    ax_hist.set_xlabel(
        r"$\log|\rho(a,b)|$  (non-null elements)",
        fontsize=10, labelpad=6, color=TEXT_DARK)
    ax_hist.set_ylabel("Frequency", fontsize=10, labelpad=6, color=TEXT_DARK)
    ax_hist.yaxis.grid(True, linestyle="--", alpha=0.7)
    ax_hist.set_axisbelow(True)
    ax_hist.tick_params(length=0)

    title_txt.set_text(
        r"Density Matrix Analysis  SNA-QDL  —  Logarithmic Scale")
    sub_txt.set_text(
        rf"$d = {d}$   ·   $n = {n}$   ·   $\lambda = {lambda_leak}$"
        rf"   ·   $n' = {n_priv}$"
        rf"   ·   $\kappa = {kappa} \,/\, {kappa_max}$"
    )
    prog_bar[0].set_width(frame_idx + 1)
    prog_txt.set_text(rf"$\kappa = {kappa}$  ({(frame_idx+1)/len(kappa_list)*100:.0f}%)")

    fig.canvas.draw_idle()
    return [im]


