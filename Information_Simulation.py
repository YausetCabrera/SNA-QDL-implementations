"""
Interactive visualization tool for the SNA-QDL protocol.

This script provides an interactive simulation and visualization framework
to study statistical properties of the density matrix arising in the SNA-QDL
protocol. The program allows the user to configure the main parameters of the 
model, including the qudit dimension (d), the number of transmitted symbols 
(n), and the leakage rate (λ). From these parameters, the effective number 
of private symbols and the range of secret-key sizes (κ) are computed.

For each value of κ, the script evaluates the logarithmic magnitude of
density matrix elements ρ(a,b) generated by the protocol under random
instances of the encoding matrix. The program then visualizes two main
aspects of the resulting distribution:

    - A heatmap representing a block of the density matrix in logarithmic scale.
    - A histogram showing the statistical distribution of non-zero
      logarithmic amplitudes log|ρ(a,b)|.

The visualization can be executed either in automatic animation mode,
where frames advance sequentially in time, or in manual mode, allowing
navigation through different κ values using control buttons. This enables
the study of how the density matrix structure evolves as the key length
increases and how key cancellation progressively sparsifies the matrix.

The tool is intended for exploratory analysis and qualitative understanding
of the locking behavior and statistical structure induced by the SNA-QDL
protocol, complementing the numerical benchmarks and theoretical results
presented in the associated research.

Dependencies: numpy, matplotlib
Authors: Yauset Cabrera-Aparicio, Jorge García-Díaz, Pino Caballero-Gil (2026)
"""

